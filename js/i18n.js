class I18n {
    constructor() {
        this.lang = localStorage.getItem('kmerschool-lang') || 'fr';
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', () => this.init());
        } else {
            this.init();
        }
    }

    init() {
        document.documentElement.setAttribute('lang', this.lang);
        this.applyTranslations();
        this.updateSwitcherUI();
    }

    setLanguage(lang) {
        this.lang = lang;
        localStorage.setItem('kmerschool-lang', lang);
        document.documentElement.setAttribute('lang', lang);
        this.applyTranslations();
        // Update UI switcher state if exists
        this.updateSwitcherUI();
    }

    applyTranslations() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (translations[this.lang][key]) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = translations[this.lang][key];
                } else if (el.tagName === 'OPTION') {
                    el.text = translations[this.lang][key];
                } else {
                    el.textContent = translations[this.lang][key];
                }
            }
        });
    }

    updateSwitcherUI() {
        // Simple text switchers
        const switchers = document.querySelectorAll('.lang-switcher-text');
        switchers.forEach(el => {
            el.textContent = this.lang.toUpperCase();
        });

        // Premium toggle switchers
        const btns = document.querySelectorAll('.lang-option');
        btns.forEach(btn => {
            const btnLang = btn.getAttribute('data-lang');
            if (btnLang === this.lang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });
    }
}

// Global instance
window.i18n = new I18n();

function toggleLanguage() {
    const newLang = window.i18n.lang === 'fr' ? 'en' : 'fr';
    window.i18n.setLanguage(newLang);
}
